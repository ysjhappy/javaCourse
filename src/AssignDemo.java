public class AssignDemo {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a+=2);
        int b = 10;
        System.out.println(b-=2);
        int c = 10;
        System.out.println(c*=2);
        int d = 10;
        System.out.println(d/=2);
        int e = 10;
        System.out.println(e%=4);
    }
}
